//Aplicaciones
console.log(sumar(1 , 6));
console.log(restar(1 , 6));
console.log(multiplicar(1 , 6));
console.log(dividir(1 , 6));

//Funciones
function sumar(a , b) {
  return a + b;
}
function restar(a , b) {
  return a - b;
}
function multiplicar(a , b) {
  return a * b;
}
function dividir(a , b) {
  return a / b;
}
